public class BitOperator {
    public static void main(String[] args) {

        // 4 -> 00000100
        // 4 << 3 -> 00100000
        System.out.println(4 << 3);

        // -2 -> 11111110(补码)
        // -2 >> 3 -> 11111111(补码)
        // -2 >> 3 -> 11111110(反码)
        // -2 >> 3 -> 10000001(原码) -> -1
        System.out.println(-2 >> 3);

        // -1 -> 11111111
        // -1 >> 2 -> 11111111
        // -1 >> 2 -> 11111110
        // -1 >> 2 -> 10000001 -> -1
        System.out.println(-1 >> 2);
        
        // -1 -> 11111111
        // -1 << 2 -> 11111100
        // -1 << 2 -> 11111011
        // -1 << 2 -> 10000100 -> -4
        System.out.println(-1 << 2);
    }
}
